export class CodebookDBHelpers {
    static async getProblems() {
        let result = await sql`SELECT json_agg(u) FROM Problems u`;
        return result[0]['json_agg'];
    }
    static async getProblemById(problemId) {
        let result = await sql`SELECT * FROM Problems WHERE problem_id = ${problemId}`;
        if (result.length > 0) {
            return result[0];
        } else {
            return null;
        }
    }
    static async createProblem(title, description) {
        let result = await sql`
            INSERT INTO Problems (title, description)
            VALUES(${ title }, ${ description })
        `;
        console.log(result);
    }
    static async createSubmission(data) {
      // data is a js object like {problemId: problemId, code: code}
      // this function should return the created record
      // placeholder return so that my code works for now
      return {
        id: 1,
        problemId: data.problemId,
        code: data.code
      };
    }
    static async getResultsById(submissionId) {
      return {
        id: submissionId,
        verdict: "Accepted"
      };
    }

    // Isabelle added this! 
    static async resetProblems() {
        try {
            // Reset the entire Database. This is quite dangerous, but works for this situation.
            await sql`TRUNCATE TABLE Problems RESTART IDENTITY CASCADE`;
        
            // Reinsert the base data
            await sql`
                INSERT INTO Problems (problem_id, title, description)
                VALUES 
                (1, 'N-Queens', '...'),
                (2, 'Two Sum', '...')
            `;

            // Set the counter to 2, since if you didn't, you'd have to submit twice before it let you submit.
            await sql`SELECT setval(pg_get_serial_sequence('Problems', 'problem_id'), 2)`;
            
            console.log("Database reset successful");
        } catch (error) {
            console.error("Failed to reset database:", error);
            throw error;
        }
    }
};
